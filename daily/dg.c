#include<stdio.h>
int index = 0;
int gongyueshu(int max,int min)
{
    if (max % min == 0) {
        return min;
        printf("min: %d\n", min);
    }
    else {
        gongyueshu(min, max % min);
        printf("语句结束，第%d层\n",index++);
    }

}

int main()
{
    int T, a, b, result ;
    scanf_s("%d", &T);
    for (int i = 0; i < T; i++)
    {
        scanf_s("%d %d", &a, &b);
        int result, max, min;

        if (a > b)
        {
            max = a;
            min = b;
        }
        else
        {
            max = b;
            min = a;
        }
        result = gongyueshu(max,min);
        printf("%d\n", result);
    }
    
}
