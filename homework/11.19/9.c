#include "stdio.h"
char layer_1[9] = {'-','-','*','*','-','*','*','-','-'};
char layer_2[9] = {'-','*','-','-','*','-','-','*','-'};
char layer_3[9] = {'-','*','-','-','-','-','-','*','-'};
char layer_4[9] = {'-','-','*','-','-','-','*','-','-'};
char layer_5[9] = {'-','-','-','*','-','*','-','-','-'};
char layer_6[9] = {'-','-','-','-','*','-','-','-','-'};
/*
 * 题目描述：平铺输出图案
 * 
 * 输入：
 * - 一行：两个正整数m和n，用空格隔开
 * 
 * 输出：
 * - 如果m或n不在1-5范围内，输出"Input Error"
 * - 否则将基础图案按m行n列平铺输出
 * 
 * 要求：
 * - 基础图案为6行8列的固定图案：
 *   "--**-**--"
 *   "-*--*--*-"
 *   "-*-----*-"
 *   "--*---*--"
 *   "---*-*---"
 *   "----*----"
 * - 平铺规则：将基础图案按m行n列重复输出
 * - 每行平铺：每行图案水平重复n次（无间隔）
 * - 整体平铺：整个6行图案垂直重复m次
 * 
 * 示例：
 * 输入: 2 3
 * 输出: 
 * --**-**----**-**----**-**--
 * -*--*--*--*--*--*--*--*--*-
 * -*-----*--*-----*--*-----*-
 * --*---*----*---*----*---*--
 * ---*-*------*-*------*-*---
 * ----*--------*--------*----
 * --**-**----**-**----**-**--
 * -*--*--*--*--*--*--*--*--*-
 * -*-----*--*-----*--*-----*-
 * --*---*----*---*----*---*--
 * ---*-*------*-*------*-*---
 * ----*--------*--------*----
 * 
 * 解释:
 * 基础图案水平重复3次，整体垂直重复2次
 * 
 * 约束：
 * - 1 ≤ m, n ≤ 5
 * - 需要验证输入合法性
 * - 图案中的'-'是减号字符
 */

void print_layer(int layer){
    switch(layer){
        case 1: 
            for(int i = 0; i < 9; i++){
                printf("%c",layer_1[i]);
            }
            break;
        case 2: 
            for(int i = 0; i < 9; i++){
                printf("%c",layer_2[i]);
            }
            break;
        case 3: 
            for(int i = 0; i < 9; i++){
                printf("%c",layer_3[i]);
            }
            break;
        case 4: 
            for(int i = 0; i < 9; i++){
                printf("%c",layer_4[i]);
            }
            break;
        case 5: 
            for(int i = 0; i < 9; i++){
                printf("%c",layer_5[i]);
            }
            break;
        case 6: 
            for(int i = 0; i < 9; i++){
                printf("%c",layer_6[i]);
            }
            break;

    }
}

int main(){
    int m,n;
    scanf("%d %d",&m,&n);
    if(n < 1 || n>5 || m < 1 || m>5){
        printf("Input Error");
        return 0;
    }
    for(int i = 1; i <= m; i++){
        for(int j = 1; j <= 6; j++){
            for(int k = 1; k <= n; k++){
                print_layer(j);
            }
            printf("\n");
        }
    }
    

}