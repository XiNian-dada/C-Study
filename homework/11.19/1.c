#include "stdio.h"
/*
 * 题目描述：判断完全数
 * 
 * 输入：
 * - 一个正整数 n (1 ≤ n ≤ 10⁸)
 * 
 * 输出：
 * - 一个整数：1 表示 n 是完全数，0 表示不是
 * 
 * 要求：
 * - 完全数定义：除自身外所有正因子之和等于 n 本身
 * - 如果 n 是完全数，输出 1；否则输出 0
 * 
 * 示例：
 * 输入: 28
 * 输出: 1
 * 解释: 28的因子（除自身外）：1, 2, 4, 7, 14，和为 1+2+4+7+14 = 28
 * 
 * 输入: 12
 * 输出: 0
 * 解释: 12的因子（除自身外）：1, 2, 3, 4, 6，和为 1+2+3+4+6 = 16 ≠ 12
 * 
 * 约束：
 * - 1 ≤ n ≤ 10⁸
 * - 注意优化因子求和算法，避免O(n)时间复杂度
 * - 提示：只需遍历到 sqrt(n)，注意完全平方数的处理
 */
int ind=0;
int num[1000];
void get_num(int input){
    for(int i = 1; i < input; i++){
        if(input % i == 0){
            num[ind++] = i;
        }
    }

}
int main()
{
    int n,sum = 0;
    int output;
    scanf("%d",&n);
    get_num(n);
    for(int i = ind-1; i >=0 ; i--){
        sum += num[i];

    }
    if(sum == n){
        printf("1");
    }else{
        printf("0");
    }
}