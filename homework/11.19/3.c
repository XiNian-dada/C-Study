#include "stdio.h"
/*
 * 题目描述：折点计数
 * 
 * 输入：
 * - 第1行：整数n (1 ≤ n ≤ 1000)，表示天数
 * - 第2行：n个整数，用空格分隔，表示每天的销售量
 * 
 * 输出：
 * - 一个整数：表示折点的数量
 * 
 * 要求：
 * - 折点定义：某天之前销售量在增长而后一天销售量减少，或者之前减少而后一天增长
 * - 保证相邻两天的销售量总是不同的（a[i-1] ≠ a[i]）
 * - 非相邻天的销售量可能相同
 * 
 * 判断规则：
 * - 对于第i天（2 ≤ i ≤ n-1）：
 *   如果 (a[i-1] < a[i] 且 a[i] > a[i+1]) → 折点（峰）
 *   如果 (a[i-1] > a[i] 且 a[i] < a[i+1]) → 折点（谷）
 * - 第1天和最后1天不能是折点
 * 
 * 示例：
 * 输入: 
 * 7
 * 5 4 1 2 3 6 4
 * 输出: 2
 * 解释: 
 * 序列：5(↓) 4(↓) 1(↑) 2(↑) 3(↑) 6(↓) 4
 * 折点：第3天（1：谷点），第6天（6：峰点）
 * 
 * 约束：
 * - 1 ≤ n ≤ 1000
 * - 销售量是不超过10000的非负整数
 * - 相邻两天销售量保证不同
 */
int main(){
    int n,index;
    int list[1001];
    scanf("%d",&n);
    for(int i=0;i<n;i++){
        scanf("%d",&list[i]);
        
    }
    index = n - 1;
    int  count = 0;
    for(int i = 1;i <index;i++){
        if((list[i] > list[i-1] && list[i] > list[i+1])
    || (list[i] < list[i-1] && list[i] < list[i+1])){
            count++;
        }
    }
    printf("%d",count);
}