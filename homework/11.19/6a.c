#include "stdio.h"
/*
 * 题目描述：将数组中的0移到末尾
 * 
 * 输入：
 * - 第1行：整数n，表示测试数据的组数
 * - 每组数据包含：
 *     - 第1行：整数m，表示数组元素个数
 *     - 第2行：m个整数，表示数组元素
 * 
 * 输出：
 * - 对每组数据：输出处理后的数组，0移到末尾，非0元素保持原顺序
 * - 元素间用空格分隔，每组输出占一行
 * 
 * 要求：
 * - 将数组中所有的0移动到数组末尾
 * - 非0元素保持原来的相对顺序
 * - 输出时元素间用空格分隔
 * 
 * 示例：
 * 输入:
 * 1
 * 10
 * 1 2 0 6 4 0 8 0 9 18
 * 输出:
 * 1 2 6 4 8 9 18 0 0 0
 * 
 * 解释:
 * 原数组: [1,2,0,6,4,0,8,0,9,18]
 * 移动后: [1,2,6,4,8,9,18,0,0,0]
 * 非0元素顺序保持不变，所有0移到末尾
 * 
 * 约束：
 * - 数组元素为整数
 * - 需要保持非0元素的原始顺序
 */
void process(int z){
    getchar();
    char c,d,e=1,f=0;
    while(1){d = getchar();if(d=='\n')break;}putchar('\b');
    while((c=getchar())!='\n'){
        if(c>'0'&&c<='9'){if(!e-1 && f==1)putchar(' ');putchar(c);e=0;f=1;}
        else if(c=='0')z++;
        else if(c==' ')e=1;
    }
    for(;z--;printf(" 0"));
    putchar('\n');
}
