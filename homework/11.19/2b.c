#include "stdio.h"
/*
 * 题目描述：数的表示（2的幂次方表示）
 * 
 * 输入：
 * - 一个十进制正整数n (1 ≤ n ≤ 100000)
 * 
 * 输出：
 * - 一行字符串：表示n的2的幂次方表达式
 * 
 * 要求：
 * - 将正整数n表示为2的幂次方的和
 * - 按幂次从高到低降序排列
 * - 格式：n=2^a+2^b+2^c+...
 * - 幂次为0时写作2^0
 * 
 * 示例：
 * 输入: 2
 * 输出: 2=2^1
 * 解释: 2 = 2¹
 * 
 * 输入: 9
 * 输出: 9=2^3+2^0
 * 解释: 9 = 8 + 1 = 2³ + 2⁰
 * 
 * 输入: 138
 * 输出: 138=2^7+2^3+2^1
 * 解释: 138 = 128 + 8 + 2 = 2⁷ + 2³ + 2¹
 * 
 * 约束：
 * - 1 ≤ n ≤ 100000
 * - 注意输出格式，等号右边按幂次降序排列
 * - 幂次为0时不要省略
 */
int main(){
    int n, first=1;
    scanf("%d",&n);
    printf("%d=",n);
    for(int i=16; i>=0; i--){
        if(n&(1<<i)){
            if(!first) printf("+");
            printf("2^%d",i);
            first = 0;
        }
    }
}