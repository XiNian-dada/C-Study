#include "stdio.h"
/*
 * 题目描述：只出现2次的数
 * 
 * 输入：
 * - 第1行：整数n (1 ≤ n ≤ 20)，表示数字个数
 * - 第2行：n个整数，用空格分隔
 * 
 * 输出：
 * - 一行：输出所有只出现2次的整数，按首次出现顺序用空格分隔
 * - 如果没有只出现2次的数，输出"none"
 * 
 * 要求：
 * - 统计每个数字出现的次数
 * - 只输出出现次数恰好为2的数字
 * - 按数字在输入中首次出现的顺序输出
 * - 多个结果用空格分隔
 * 
 * 示例：
 * 输入: 
 * 5
 * 5 2 3 4 3
 * 输出: 3
 * 解释: 数字出现次数：5(1), 2(1), 3(2), 4(1) → 只有3出现2次
 * 
 * 输入:
 * 6
 * 2 3 3 2 5 6
 * 输出: 2 3
 * 解释: 数字出现次数：2(2), 3(2), 5(1), 6(1) → 2和3都出现2次，按首次出现顺序输出
 * 
 * 输入:
 * 4
 * 3 4 5 6
 * 输出: none
 * 解释: 所有数字都只出现1次
 * 
 * 约束：
 * - 1 ≤ n ≤ 20
 * - 需要保持首次出现的顺序
 */
int main(){
    int n;
    int find = 0;
    int list[1001];
    for(int i=0;i<1001;i++){
        list[i] = 0;
    }
    scanf("%d",&n);
    int temp;
    while(scanf("%d",&temp) != EOF){
        list[temp]++;
    }
    for(int i = 0;i < 1001;i++){
        if(list[i] == 2){
            printf("%d ",i);
            find = 1;
        }
    }
    if(find == 0){
        printf("none");
    }
}