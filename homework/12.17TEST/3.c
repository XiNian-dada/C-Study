#include "stdio.h"
#include "stdlib.h"
#include "string.h"
#include "math.h"
#include "ctype.h"
/*
 * 题目描述：计算数列和
 * 
 * 难度：简单
 * 
 * 输入：
 * - 一行：一个正整数n（1 ≤ n ≤ 10⁴）
 * 
 * 输出：
 * - 一行：交错级数的前n项和，保留4位小数（四舍五入）
 * 
 * 数列定义：
 * S = 1 − 1/2 + 1/3 − 1/4 + 1/5 − 1/6 + … ± 1/n
 * 
 * 符号规则：
 * - 第1项（i=1）：正号
 * - 第2项（i=2）：负号
 * - 第3项（i=3）：正号
 * - 第4项（i=4）：负号
 * - ...
 * - 通项公式：(-1)^{i+1} × (1/i)
 * 
 * 最后一项符号：
 * - 当n为奇数时，最后一项为正
 * - 当n为偶数时，最后一项为负
 * 
 * 示例1：
 * 输入:
 * 1
 * 输出:
 * 1.0000
 * 
 * 解释:
 * S = 1 = 1.0000
 * 
 * 示例2：
 * 输入:
 * 5
 * 输出:
 * 0.7833
 * 
 * 解释:
 * S = 1 - 1/2 + 1/3 - 1/4 + 1/5
 *   = 1 - 0.5 + 0.3333 - 0.25 + 0.2
 *   = 0.7833
 * 
 * 计算过程：
 * 项数 符号 值       累计和
 * 1    +   1.0000   1.0000
 * 2    -   0.5000   0.5000
 * 3    +   0.3333   0.8333
 * 4    -   0.2500   0.5833
 * 5    +   0.2000   0.7833
 * 
 * 约束：
 * - 1 ≤ n ≤ 10000
 * - 需要保留4位小数
 * - 使用浮点数计算，注意精度
 * 
 * 提示：
 * 1. 使用double类型保证精度
 * 2. 可以使用(-1)^(i+1)确定符号
 * 3. 或使用if判断：i为奇数加，i为偶数减
 * 4. 输出格式：printf("%.4f\n", sum);
 */
int main(){
    int n;
    scanf("%d", &n);
    double sum = 0.0;
    int have_neg = 0;
    for(int i = 1; i <= n; i++){
        if(have_neg == 0){
            sum += 1.0/i;
            have_neg = 1;
        }else{
            sum -= 1.0/i;
            have_neg = 0;
        }
    }
    printf("%.4f", sum);
}