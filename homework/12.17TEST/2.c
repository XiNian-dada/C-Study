#include "stdio.h"
#include "stdlib.h"
#include "string.h"
#include "math.h"
#include "ctype.h"
/*
 * 题目描述：绝对值的和
 * 
 * 难度：简单
 * 
 * 输入：
 * - 一行：4个整数，范围在-100000到100000之间
 * 
 * 输出：
 * - 一个整数：4个整数的绝对值的和
 * 
 * 计算公式：
 * 如果输入四个整数为a, b, c, d
 * 则输出 = |a| + |b| + |c| + |d|
 * 
 * 示例：
 * 输入:
 * 1 2 -3 4
 * 输出:
 * 10
 * 
 * 解释：
 * |1| + |2| + |-3| + |4| = 1 + 2 + 3 + 4 = 10
 * 
 * 约束：
 * - 输入保证是4个整数
 * - 每个整数范围：-100000 ≤ x ≤ 100000
 * - 结果在int范围内（最大4×100000=400000）
 * 
 * 注意：
 * - 使用abs()函数或条件判断计算绝对值
 * - 注意负数的情况
 */
int main(){
    int a,b,c,d;
    scanf("%d %d %d %d", &a, &b, &c, &d);
    a = abs(a);
    b = abs(b);
    c = abs(c);
    d = abs(d);
    printf("%d\n", a + b + c + d);
}


