#include "stdio.h"
#include "stdlib.h"
#include "string.h"
#include "ctype.h"
/*
 * 题目描述：打印柱状图
 * 
 * 难度：简单+
 * 
 * 输入：
 * - 一行数据：一个正整数n，后面跟随n个整数
 * - 格式：n m1 m2 ... mn（以空格分隔）
 * 
 * 输出：
 * - 如果输入合法：打印柱状图
 * - 如果输入不合法：输出"Input Error"
 * 
 * 规则：
 * 1. 柱状图由n列组成
 * 2. 每列的高度由对应的整数值决定（0-10）
 * 3. 柱状图的总高度由输入数据中的最大值决定
 * 4. 输出从顶部到底部逐行打印：
 *    - 如果该行在对应列的高度范围内，打印"*"
 *    - 如果该行超出对应列的高度，打印"-"
 *    - 同一行的字符直接连接，不需要空格
 * 
 * 合法输入条件：
 * - 第一个数n满足：1 ≤ n ≤ 10
 * - 后面跟随的n个整数满足：0 ≤ m ≤ 10
 * - 输入格式正确，数据个数匹配
 * 
 * 示例1：
 * 输入:
 * 4 1 3 2 4
 * 输出:
 * ---*
 * -*-*
 * -***
 * ****
 * 
 * 解释:
 * n=4，数据为[1,3,2,4]
 * 最大值=4，所以柱状图有4行
 * 第1行（顶部）：高度≥4的列打印*，其他打印- → ---*（只有第4列高度为4）
 * 第2行：高度≥3的列打印* → -*-*（第2列高度3，第4列高度4）
 * 第3行：高度≥2的列打印* → -***（第2列高度3，第3列高度2，第4列高度4）
 * 第4行：高度≥1的列打印* → ****（所有列高度都≥1）
 * 
 * 示例2：
 * 输入:
 * 5 0 5 2 3 1
 * 输出:
 * -*---
 * -*---
 * -*-*-
 * -***-
 * -****
 * 
 * 解释:
 * n=5，数据为[0,5,2,3,1]
 * 最大值=5，柱状图有5行
 * 从顶部到底部逐行输出
 * 
 * 示例3：
 * 输入:
 * 3 0 4 1
 * 输出:
 * -*-
 * -*-
 * -*-
 * -**
 * 
 * 解释:
 * n=3，数据为[0,4,1]
 * 最大值=4，柱状图有4行
 * 
 * 约束：
 * - 1 ≤ n ≤ 10
 * - 0 ≤ m ≤ 10
 * - 需要验证输入数据的合法性
 * - 需要处理输入格式错误的情况
 */
int main(){
    int n;
    scanf("%d", &n);
    if(n<=0 || n>10){
        printf("Input Error");
        return 0;
    }
    int* list = (int*)malloc(n*sizeof(int));
    int max = 0;
    for(int i=0; i<n; i++){
        scanf("%d", &list[i]);
        if(list[i]<0 || list[i]>10){
            printf("Input Error");
            return 0;
        }
        max = max>list[i]?max:list[i];
    }
    for(int i = max; i>0; i--){
        for(int j = 0; j<n; j++){
            if(list[j] >=i){
                printf("*");
                continue;
            }else{
                printf("-");
                continue;
            }
        }
        printf("\n");
    }
    
    
}

