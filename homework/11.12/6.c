#include "stdio.h"
#define ll long long
/*
 * 题目描述：递归实现n!
 * 
 * 输入：
 * - 多组数据，每组数据一个整数n (n <= 20)
 * - 以EOF结束输入
 * 
 * 输出：
 * - 对于每组数据，输出n!的值
 * 
 * 要求：
 * - 必须使用递归函数计算阶乘
 * - 程序中不能出现循环语句
 * - 每组输出占一行
 * 
 * 示例：
 * 输入:
 * 4
 * 5
 * 输出:
 * 24
 * 120
 * 
 * 解释:
 * 4! = 4 × 3 × 2 × 1 = 24
 * 5! = 5 × 4 × 3 × 2 × 1 = 120
 * 
 * 约束：
 * - n ≤ 20
 * - 输入以EOF结束
 * - 必须使用递归实现，不能使用循环
 */
ll jc(int n){
    if (n<=1){
        return 1;
    
    } else {
        return n*jc(n-1);

    }
    
}

int main(){
    int n;
    while(scanf("%d",&n)!=EOF){
        printf("%lld\n",jc(n));
    }
}