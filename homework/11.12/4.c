#include "stdio.h"
/*
 * 题目描述：乘法表
 * 
 * 输入：
 * - 第1行: 整数n, 表示测试数据的组数
 * - 随后n行: 每行一个整数m (1 ≤ m ≤ 9)
 * 
 * 输出：
 * - 对每组数据，输出m*m乘法表
 * - 格式：每行输出多个等式，等式之间用空格隔开，每行输出完毕换行
 * 
 * 要求：
 * - 输出从1*1到m*m的乘法表
 * - 每行格式为：i*1=i i*2=2i ... i*i=i²
 * - 行内等式用空格分隔
 * 
 * 示例：
 * 输入:
 * 2
 * 1
 * 2
 * 输出:
 * 1*1=1
 * 1*1=1 2*2=4
 * 
 * 解释:
 * 当m=1时，只有1*1=1
 * 当m=2时，第一行：1*1=1，第二行：2*1=2 2*2=4
 * 
 * 约束：
 * - 1 ≤ m ≤ 9
 */
int main(){
    int n;
    scanf ("%d", &n);
    for(int i = 1; i <= n; i++){
        int a;
        scanf ("%d", &a);
        for(int j = 1; j <= a; j++){
            for(int k = 1; k <= a && k<= j; k++){
                printf("%d*%d=%d ", j, k, j*k);
            }
            printf("\n");
        }
    }
}