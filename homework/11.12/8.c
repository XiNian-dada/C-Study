#include "stdio.h"
/*
 * 题目描述：统计星号
 * 
 * 输入：
 * - 一行：字符串s (1 ≤ |s| ≤ 1000)，包含小写字母、竖线'|'和星号'*'
 * 
 * 输出：
 * - 一个整数：表示不在竖线对之间的星号'*'的数量
 * 
 * 要求：
 * - 每两个连续的竖线'|'为一对（第1个和第2个为一对，第3个和第4个为一对，依此类推）
 * - 统计所有不在竖线对之间的星号数量
 * - 字符串保证包含偶数个竖线
 * 
 * 核心算法：
 * - 使用布尔状态标记当前是否位于竖线对内部
 * - 遇到竖线'|'时切换状态
 * - 只在非竖线对内部状态时统计星号'*'
 * 
 * 示例：
 * 输入: "l|eet|c**o|*de|"
 * 输出: 2
 * 解释: 字符串分解：l |eet| c**o |*de| 
 *       竖线对之间：eet, *de
 *       竖线对之外：l, c**o → 星号位置：c**o中有2个星号
 * 
 * 输入: "yo|uar|e**|b|e***au|tifu|l"
 * 输出: 5
 * 解释: 竖线对之间：uar, b, tifu
 *       竖线对之外：yo, e**, e***au, l → 星号位置：e**有2个，e***au有3个，共5个
 * 
 * 约束：
 * - 字符串长度1-1000
 * - 保证竖线数量为偶数
 * - 只包含小写字母、竖线'|'和星号'*'
 */
int main(){
    int is_in_pair = 0;
    char c;
    int count = 0;
    while(((c = getchar())!= '\n')){
        if (c == '*' && is_in_pair == 0){
            count++;
        }
        if(c == '|'){
            is_in_pair = !is_in_pair;
        }
    }
    printf("%d", count);
}