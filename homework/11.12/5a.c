/*
 * 题目描述：交换十进制整数的相邻奇偶位
 * 
 * 输入：
 * - 一个32位带符号整数n
 * 
 * 输出：
 * - 一个整数，表示交换相邻奇偶位后的结果
 * 
 * 要求：
 * - 将n的十进制表示中奇数位与偶数位数字交换
 * - 奇数位：从左往右数第1、3、5...位
 * - 偶数位：从左往右数第2、4、6...位
 * - 保持原符号不变
 * - 忽略前导零
 * 
 * 示例：
 * 输入: 1234
 * 输出: 2143
 * 解释: 奇数位(1,3)与偶数位(2,4)交换 → 2,1,4,3 → 2143
 * 
 * 输入: -123
 * 输出: -213
 * 解释: 取绝对值123 → 奇数位(1,3)与偶数位(2)交换 → 2,1,3 → 213 → 加符号-213
 * 
 * 约束：
 * - n为32位带符号整数
 * - 需要考虑负数的处理
 */
#include "stdio.h"
#include "string.h"

int main() {
    char number[35];
    scanf("%s", number);
    for (char* n=number+(number[0]=='-');n-number<strlen(number)-1;*n=*n^*(n+1),*(n+1)=*n^*(n+1),*n=*n^*(n+1),n+=2);
    if (number[0] == '-' && number[1] == '0') number[1] = '-';
    printf("%s\n", number+((number[0]=='0'&&number[1]!=0)||number[1]=='-'));
    return 0;
}