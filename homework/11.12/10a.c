/*
 * 题目描述：阶乘求和取模
 * 
 * 输入：
 * - 第1行: 整数k (1 ≤ k ≤ 100)，表示测试数据的组数
 * - 随后k行: 每行一个正整数n (1 ≤ n ≤ 1000000)
 * 
 * 输出：
 * - k行: 每行一个整数，表示f(n) = (1! + 2! + ... + n!) % 1000000
 * 
 * 要求：
 * - 计算前n项阶乘之和并对1000000取模
 * - 即 f(n) = (∑(i=1 to n) i!) % 1000000
 * 
 * 注意：
 * - 当n ≥ 25时，n!的末尾会有多个0，且n! % 1000000 = 0
 * - 因此当n ≥ 25时，f(n) = f(24) （因为25!及之后的阶乘对1000000取模都为0）
 * - 实际只需要计算到min(n, 24)即可
 * 
 * 示例：
 * 输入:
 * 3
 * 1
 * 5
 * 10
 * 输出:
 * 1
 * 153
 * 37913
 * 
 * 解释:
 * f(1) = 1! % 1000000 = 1
 * f(5) = (1!+2!+3!+4!+5!) % 1000000 = (1+2+6+24+120) % 1000000 = 153
 * f(10) = (1!+2!+...+10!) % 1000000 = 37913
 * 
 * 约束：
 * - 1 ≤ k ≤ 100
 * - 1 ≤ n ≤ 1000000
 * - 模数为1000000
 * - 需要利用模运算性质和阶乘特性进行优化
 */
#include "stdio.h"

int main() {
    int k;
    scanf("%d", &k);
    for (int i=0,n,sum=1,result=0;i<k;i++,sum=1,result=0) {
        scanf("%d", &n);
        for (int j=0;j<n;j++,sum=sum*j%1000000,result=(result+sum)%1000000);
        printf("%d\n", result);
    }

    return 0;
}