#include <stdio.h>
/*
 * 题目描述：整数分数的加法
 * 
 * 输入：
 * - 第1行: 整数n, 表示测试数据的组数
 * - 随后n行: 每行一个整数m (m > 0)，表示要计算的前m项
 * 
 * 输出：
 * - n行: 每行一个浮点数，表示1/2 + 2/3 + 3/4 + ... 前m项的和，保留4位小数
 * 
 * 要求：
 * - 计算序列：1/2 + 2/3 + 3/4 + 4/5 + ... 的前m项之和
 * - 结果保留4位小数
 * - 每组输出占一行
 * 
 * 示例：
 * 输入: 
 * 1
 * 2
 * 输出:
 * 1.1667
 * 
 * 解释:
 * 当m=2时: 1/2 + 2/3 = 0.5 + 0.6667 ≈ 1.1667
 * 
 * 约束：
 * - m为整数且m > 0
 * - n为整数，表示测试用例数量
 */
int main(){
    //初始化行数和项数
    int n;
    int m[1000] = {-1};
    //输入行数和项数
    scanf("%d", &n);
    for (int i = 1; i <= n; i++){
        scanf("%d", &m[i]);
    }
    //计算每行项数和
    for (int i = 1; i <= n; i++){
        double sum = 0.0;
        for (int j = 1; j <= m[i]; j++){
            sum += (double) (j / (j+1.0));
        }
        printf("%.4f\n", sum);

    }

}