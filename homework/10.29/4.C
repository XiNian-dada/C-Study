#include <stdio.h>
/*
 * 题目描述：分数的前n项值
 * 
 * 输入：
 * - 第1行: 整数n, 表示测试数据的组数
 * - 随后n行: 每行一个整数m (m > 0)，表示要计算的前m项
 * 
 * 输出：
 * - n行: 每行一个浮点数，表示序列 1/3 - 2/5 + 3/8 - 4/12 + 5/17 ... 前m项的和，保留4位小数
 * 
 * 要求：
 * - 计算序列：1/3 - 2/5 + 3/8 - 4/12 + 5/17 ... 的前m项之和
 * - 符号规律：奇数项为正，偶数项为负
 * - 分子规律：第k项的分子为k
 * - 分母规律：分母序列为3, 5, 8, 12, 17...（从3开始，每次增加2, 3, 4, 5...）
 * - 结果保留4位小数
 * - 每组输出占一行
 * 
 * 示例：
 * 输入: 
 * 2
 * 1
 * 2
 * 输出:
 * 0.3333
 * -0.0667
 * 
 * 解释:
 * 当m=1时: 1/3 ≈ 0.3333
 * 当m=2时: 1/3 - 2/5 = 0.3333 - 0.4 = -0.0667
 * 
 * 约束：
 * - m为整数且m > 0
 * - n为整数，表示测试用例数量
 */
int main(){
    //声明行数和每组的项数
    int n,a[1000];
    scanf("%d",&n);
    for (int i = 0; i < n; i++){
        scanf("%d",&a[i]);
    }
    //开始循环，从索引0开始
    for (int i = 0; i < n; i++){
        double sum = 0.0;
        for (int j = 1; j <= a[i]; j++){
            //如果为偶数
            if (j % 2 == 0){ 
                sum -= (double) j/ ((j * (j+1))/2 + 2);
            } else {
                //反之为奇数时
                sum += (double) j/ ((j * (j+1))/2 + 2);
            }

        }
        printf("%.4lf\n",sum);
    }
}