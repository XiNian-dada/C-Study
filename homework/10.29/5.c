#include <stdio.h>
/*
 * 题目描述：二进制包含几个1
 * 
 * 输入：
 * - 第1行: 整数n, 表示测试数据的组数
 * - 随后n行: 每行一个正整数
 * 
 * 输出：
 * - n行: 每行一个整数，表示对应数字的二进制表示中1的个数
 * 
 * 要求：
 * - 计算每个正整数转换为二进制后，其中包含的1的个数
 * - 每组输出占一行
 * 
 * 示例：
 * 输入: 
 * 2
 * 10
 * 100
 * 输出:
 * 2
 * 3
 * 
 * 解释:
 * 10的二进制是1010，包含2个1
 * 100的二进制是1100100，包含3个1
 * 
 * 约束：
 * - 输入为正整数
 * - n为整数，表示测试用例数量
 */
int main() {
    //处理输入
    int n;
    scanf("%d", &n);
    int input[1000];
    for(int i = 0; i < n; i++){
        scanf("%d", &input[i]);
    }
    //开始处理
    //根据/2取余法，计算有多少次余数为1，给temp +=1，直到input[i]为0
    for (int i = 0; i < n; i++){
        int temp = 0;
        while (input[i] > 0){
            if (input[i] % 2 ==0){
                input[i] /= 2;
            } else {
                input[i] -= 1;
                temp++;
            }
        }
        printf("%d\n", temp);
    }

}