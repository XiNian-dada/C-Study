#include <stdio.h>
/*
 * 题目描述：统计元音字母
 * 
 * 输入：
 * - 一行，一个长度不超过1000的可见ASCII字符串（不包含空格）
 * 
 * 输出：
 * - 如果存在元音字母：按出现顺序输出这些元音字母（大写转小写，用"-"连接），最后输出总次数
 *   格式：v1-v2-...-vk-count
 * - 如果没有任何元音字母：仅输出0
 * 
 * 要求：
 * - 统计元音字母：A、E、I、O、U（大小写均算）
 * - 大写字母统一转换为小写输出
 * - 按元音字母在字符串中的出现顺序输出
 * - 相邻元音字母用"-"连接
 * - 最后输出总次数
 * - 如果没有元音字母，只输出0
 * 
 * 示例：
 * 输入: "Hello,World!"
 * 输出: "e-o-o-3"
 * 解释: 元音字母依次是e, o, o，共3个
 * 
 * 输入: "Rhythm"
 * 输出: "0"
 * 解释: 没有元音字母
 * 
 * 约束：
 * - 字符串长度 ≤ 1000
 * - 仅包含可见ASCII字符，不包含空格
 */
int main(){
    char c[10000];
    int i = 0;
    
    char ch;
    //读取ch，并将其存入字符串c
    while ((ch = getchar()) != '\n' && i < 9999) {
        c[i] = ch;
        i++;
    }
    i--; 
    //跳出循环的最后一次还有i++，所以要-1

    int j = 0;
    int temp = 0;
    //正序遍历
    while (j <= i) {
        //如果是字母，那就输出这个 a-
        if (c[j] == 'a' || c[j] == 'e' || c[j] == 'i' ||
            c[j] == 'o' || c[j] == 'u'){
                printf("%c-",c[j]);
                temp++;
        }
        //转小写
        if (c[j] == 'A' || c[j] == 'E' || c[j] == 'I' ||
            c[j] == 'O' || c[j] == 'U'){
                printf("%c-",c[j]+32);
                temp++;
            }
        //开始下一位
        j++;
        
        
    }
    printf("%d",temp);
    return 0;
}