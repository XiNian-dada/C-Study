#include <stdio.h>
/*
 * 题目描述：偶数分数加法
 * 
 * 输入：
 * - 第1行: 整数m, 表示测试数据的组数
 * - 随后m行: 每行一个偶数n (n > 0)
 * 
 * 输出：
 * - m行: 每行一个浮点数，表示1/2 + 1/4 + ... + 1/n的和，保留4位小数
 * 
 * 要求：
 * - 计算从1/2开始到1/n的所有偶数倒数之和
 * - 结果保留4位小数
 * - 每组输出占一行
 * 
 * 示例：
 * 输入: 
 * 2
 * 6
 * 8
 * 输出:
 * 0.9167
 * 1.0417
 * 
 * 解释:
 * 当n=6时: 1/2 + 1/4 + 1/6 = 0.5 + 0.25 + 0.1667 ≈ 0.9167
 * 当n=8时: 1/2 + 1/4 + 1/6 + 1/8 = 0.5 + 0.25 + 0.1667 + 0.125 = 1.0417
 * 
 * 约束：
 * - n为偶数且n > 0
 * - m为整数，表示测试用例数量
 */
int main(){
    int n;                    // 测试数据组数
    scanf("%d", &n);
    int input[1000];          // 存储每组测试数据的n值
    double sum[1000] = {0};   // 存储每组计算结果，初始化为0
    
    // 读取n组测试数据
    for (int i = 0; i < n; i++){
        scanf("%d", &input[i]);
    }
    
    // 对每组数据计算偶数倒数和
    for (int i = 0; i<n;i++){
        // 从2开始，每次递增2，直到input[i]，累加1/j
        for (int j = 2; j <= input[i]; j+=2){
            sum[i] += (double) 1.0/j;
        }
    }
    
    // 输出每组计算结果，保留4位小数
    for (int i = 0; i < n; i++){
        printf("%.4f\n", sum[i]);
    }
    
    return 0;
}