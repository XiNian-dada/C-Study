#include <stdio.h>

/*
 * 题目：判断直角三角形
 * 
 * 描述：
 * 输入三角形的3条边长，判断三角形是否是直角三角形。
 * 
 * 输入：
 * 第1行是一个整数n，表示随后有n组数据。
 * 每组数据占一行，包含3个整数代表3条边长(每个整数的平方能够用32位整数表示)，用空格分隔。
 * 
 * 输出：
 * 每组输出占一行，如果是直角三角形输出Yes，不是则输出No。
 * 
 * 样例：
 * 输入:
 * 2
 * 3 4 5   
 * 3 5 7
 * 
 * 输出:
 * Yes
 * No
 */

int main(){
    // 声明变量n表示数据组数，声明三个数组a、b、c分别存储三角形的三条边
    int n ,a[100],b[100],c[100];
    
    // 读取数据组数n
    scanf("%d",&n);
    
    // 循环读取n组三角形的三边长度数据
    for (int i=0;i<n;i++){
        scanf("%d %d %d",&a[i],&b[i],&c[i]);
    }
    
    // 对每组数据进行判断是否为直角三角形
    for (int i=0;i<n;i++){
        // 检查三边长度是否都为正数，若存在非正数则不可能构成三角形
        if (a[i] <= 0 || b[i] <= 0 || c[i] <= 0){
            printf("No\n");
            continue;  // 跳过本次循环，处理下一组数据
        }
        
        // 检查是否满足三角形构成条件（任意两边之和大于第三边）
        if (a[i] + b[i] <= c[i] || a[i] + c[i] <= b[i] || b[i] + c[i] <= a[i]){
            printf("No\n");
            continue;  // 不满足三角形条件，跳过本次循环
        }
        
        // 计算三边长度的平方值，用于勾股定理验证
        int temp_a = a[i] * a[i];  // 第一边的平方
        int temp_b = b[i] * b[i];  // 第二边的平方
        int temp_c = c[i] * c[i];  // 第三边的平方
        
        // 使用勾股定理判断是否为直角三角形
        // 直角三角形满足：两短边平方和等于最长边平方
        if (temp_a + temp_b == temp_c || temp_a + temp_c == temp_b || temp_b + temp_c == temp_a){
            printf("Yes\n");  // 满足勾股定理，是直角三角形
        }
        else{
            printf("No\n");   // 不满足勾股定理，不是直角三角形
        }
    }
    
    // 程序正常结束，返回0
    return 0;
}