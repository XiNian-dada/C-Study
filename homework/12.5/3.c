#include "stdio.h"
#include "stdlib.h"

/*
 * 题目描述：画棋盘
 * 
 * 难度：简单+
 * 
 * 输入：
 * - 第1行：4个整数 n m q k，用空格分隔
 *   - n：棋盘的行数
 *   - m：棋盘的列数
 *   - q：需要画横线的行数
 *   - k：需要画竖线的列数
 * - 第2行：q个整数，表示需要画横线的行号（从1开始计数）
 * - 第3行：k个整数，表示需要画竖线的列号（从1开始计数）
 * 
 * 输出：
 * - 输出n行，每行m个字符，表示画线后的棋盘
 * - 字符规则：
 *   1. 普通格子（无横线无竖线）：'0'
 *   2. 只有横线：'-'
 *   3. 只有竖线：'|'
 *   4. 既有横线又有竖线（交叉点）：'+'
 * 
 * 棋盘表示规则：
 * - 棋盘大小为n×m
 * - 需要在指定的行画横线（整行都画）
 * - 需要在指定的列画竖线（整列都画）
 * - 当某个格子同时有横线和竖线时，显示为'+'
 * 
 * 示例1：
 * 输入:
 * 5 5 2 2
 * 1 3
 * 4 5
 * 输出:
 * ---++
 * 000||
 * ---++
 * 000||
 * 000||
 * 
 * 解释：
 * 棋盘5行5列
 * 需要画横线的行：第1行、第3行
 * 需要画竖线的列：第4列、第5列
 * 
 * 第1行：整行都有横线，但第4、5列有竖线交叉，所以：
 *   第1-3列：'-'（只有横线）
 *   第4列：'+'（横竖交叉）
 *   第5列：'+'（横竖交叉）
 * 第2行：无横线，但第4、5列有竖线：
 *   第1-3列：'0'
 *   第4列：'|'
 *   第5列：'|'
 * 第3行：同第1行
 * 第4、5行：同第2行
 * 
 * 示例2：
 * 输入:
 * 8 8 3 5
 * 2 7 4
 * 8 2 5 3 2
 * 输出:
 * 0||0|00|
 * -++-+--+
 * 0||0|00|
 * -++-+--+
 * 0||0|00|
 * 0||0|00|
 * -++-+--+
 * 0||0|00|
 * 
 * 解释：
 * 需要画横线的行：2, 7, 4
 * 需要画竖线的列：8, 2, 5, 3, 2（注意：第2列重复，仍算一次）
 * 根据行列交叉关系输出相应字符
 * 
 * 约束：
 * - 1 ≤ n, m ≤ 1000
 * - 0 ≤ q ≤ min(n, 1000000)
 * - 0 ≤ k ≤ min(m, 1000000)
 * - 需要画线的行列号在[1, n]或[1, m]范围内
 * - 行列号可能重复，重复的按一次处理
 * - 时间复杂度需优化，不能直接O(n×m×q×k)
 * 
 * 提示：
 * - 使用两个标记数组：row_mark[n]标记哪些行要画横线，col_mark[m]标记哪些列要画竖线
 * - 对于每个格子(i,j)，根据row_mark[i]和col_mark[j]决定输出字符
 * - 注意行列编号从1开始，数组下标从0开始
 */
int main(){
    int n,m,q,k;
    scanf("%d %d %d %d",&n,&m,&q,&k);
    int a[n][m];
    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            a[i][j] = 0;
        }
    }
    for(int i = 0; i < q; i++){
        int x;
        scanf("%d",&x);
        for(int j = 0; j < m ; j++){
            a[x-1][j] += 1;
        }
    }
    for (int i = 0; i < k; i++){
        int y;
        scanf("%d",&y);
        for(int j = 0; j < n; j++){
            a[j][y-1] += 2;
        }
    }

    for(int i = 0; i < n; i++){
        for(int j = 0; j < m; j++){
            if(a[i][j] == 0){
                printf("0");
            }else if(a[i][j] == 1){
                printf("-");
            }else if(a[i][j] == 2){
                printf("|");
            }else if(a[i][j] == 3){
                printf("+");
            }
        }
        printf("\n");

    }
}