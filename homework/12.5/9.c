#include "stdio.h"
#include "stdlib.h"
#include "string.h"
#include "math.h"
#define ll long long
/*
 * 题目描述：秘密解密
 * 
 * 难度：简单+
 * 
 * 输入：
 * - 一个整数n，表示加密后的值
 * 
 * 输出：
 * - 一个整数x，满足方程：6×x³ + 2×x² - x = n
 * 
 * 方程：
 * f(x) = 6x³ + 2x² - x = n
 * 其中x为正整数，n为已知的加密值
 * 
 * 要求：
 * - 找到满足方程的正整数解x
 * - 题目保证存在正整数解
 * - 只需要输出一个解（题目保证解唯一）
 * 
 * 示例1：
 * 输入:
 * 6190
 * 输出:
 * 10
 * 
 * 验证：
 * f(10) = 6×1000 + 2×100 - 10 = 6000 + 200 - 10 = 6190
 * 
 * 示例2：
 * 输入:
 * 11288500567455
 * 输出:
 * 12345
 * 
 * 验证：
 * f(12345) = 6×12345³ + 2×12345² - 12345 = 11288500567455
 * 
 * 约束：
 * - n ≤ 10^9
 * - x为正整数
 * - 保证存在正整数解
 * 
 * 解法分析：
 * 1. 方程：6x³ + 2x² - x - n = 0
 * 2. 由于x为正整数，可以直接枚举x
 * 3. 当x增大时，6x³增长很快，所以x的范围不会太大
 * 4. 对于n ≤ 10^9，x的最大值估算：
 *    6x³ ≤ n → x ≤ ∛(n/6) ≈ ∛(1.67×10^8) ≈ 550
 * 5. 所以x的范围大约是1到550
 * 6. 可以直接从1开始枚举，直到找到满足条件的x
 * 
 * 优化：
 * 可以使用二分查找加快速度：
 * - 下界：1
 * - 上界：∛(n/6) + 1
 * - 每次计算f(mid)与n比较
 * 
 * 注意：
 * - 计算f(x)时可能溢出，使用long long类型
 * - 可以直接计算：6LL*x*x*x + 2LL*x*x - x
 * - 不需要使用sqrt或cbrt函数
 * 
 * 时间复杂度：
 * - 枚举法：O(∛n)，最多约550次计算
 * - 二分法：O(log(∛n))，最多约10次计算
 */
int main(){
    ll input;
    scanf("%lld", &input);
    ll max_factor = cbrt(input);
    for(ll i = 1; i <= max_factor; i++){
        if(6*i*i*i + 2*i*i - i == input){
            printf("%d",i);
            return 0;
        }
    }
}