#include <stdio.h>

/*
 * 题目描述：韩信点兵（中国剩余定理）
 *
 * 输入：
 * - 第1行: 整数n, 表示测试数据的组数
 * - 随后n行: 每行三个整数a, b, c，表示3人一排队尾、5人一排队尾、7人一排队尾人数
 *
 * 输出：
 * - 对每组数据，输出总人数（在10到100之间）
 *   如果无解，输出"Impossible"
 *
 * 要求：
 * - 总人数x满足：
 *     x % 3 = a
 *     x % 5 = b
 *     x % 7 = c
 * - 10 ≤ x ≤ 100
 * - 如果存在多个解，输出任意一个
 * - 如果无解输出Impossible
 *
 * 示例：
 * 输入:
 * 2
 * 1 2 3
 * 2 3 2
 * 输出:
 * 52
 * 23
 *
 * 输入:
 * 3
 * 1 1 2
 * 2 4 6
 * 2 3 6
 * 输出:
 * 16
 * Impossible
 * 83
 *
 * 约束：
 * - 总人数范围 [10, 100]
 */
int main(){
    int n;
    int a,b,c;
    scanf("%d", &n);
    for (int i = 0; i < n; i++){
        scanf("%d %d %d", &a, &b, &c);
        for (int j = 10; j <=100; j++){

            if(j % 3 == a && j % 5 == b && j % 7 == c){ //满足余数条件
                printf("%d\n", j);
                break;
            }
            if(j == 100){
                printf("Impossible\n"); //无解
            }
        }
    }
    
}