#include <stdio.h>
/*
 * 题目描述：解不等式（求最大n）
 *
 * 输入：
 * - 一行: 一个32位无符号整数m (2 ≤ m ≤ 10⁸)
 *
 * 输出：
 * - 一行: 一个整数n，满足 1² + 2² + ... + n² < m 的最大正整数n
 *
 * 要求：
 * - 求最大的正整数n，使得前n个正整数的平方和小于m
 * - 使用公式: 1²+2²+...+n² = n(n+1)(2n+1)/6
 *
 * 示例：
 * 输入: 30
 * 输出: 3
 * 解释: 1²+2²+3²=14<30, 1²+2²+3²+4²=30≥30 ⇒ 最大n=3
 *
 * 输入: 2
 * 输出: 1
 * 解释: 1²=1<2, 1²+2²=5≥2 ⇒ 最大n=1
 *
 * 约束：
 * - 2 ≤ m ≤ 10⁸
 */
int main(){ //很简单 没啥解释的
    int m,n = 0;
    int sum = 0;
    scanf("%d",&m);
    while (sum < m){
        sum += n*n;
        n++;
    }
    printf("%d", n - 2);
}