#include <stdio.h>
/*
 * 题目描述：冰雹猜想（Collatz猜想）
 *
 * 输入：
 * - 一行: 一个正整数n (1 ≤ n ≤ 1000000)
 *
 * 输出：
 * - 一行: 一个整数
 *   如果在10步内得到1，输出步数；否则输出-1
 *
 * 要求：
 * - 反复执行变换：
 *     n为偶数 → n = n / 2
 *     n为奇数 → n = 3n + 1
 * - 记录从n变换到1所需的步数
 * - 如果10步内未得到1，输出-1
 *
 * 示例：
 * 输入: 3
 * 输出: 7
 * 解释: 3→10→5→16→8→4→2→1 共7步
 *
 * 输入: 7
 * 输出: -1
 * 解释: 7→22→11→34→17→52→26→13→40→20→10... 超过10步未到1
 *
 * 约束：
 * - 1 ≤ n ≤ 1000000
 */
int main(){
    int n;
    scanf ("%d", &n);
    
    int count = 0;
    
    while(n != 1){
        if (count >= 10){ //十步无解
            printf("-1");
            return 0;
        }
        
        if (n % 2 == 0){ //偶数除二
            n = n / 2;
        } else {
            n = (3 * n) + 1; //奇数操作
        }
        count++; //计数器+1
    }
    
    if (count <= 10){
        printf("%d", count);
    } else {
        printf("-1");
    }
    
    return 0;
}