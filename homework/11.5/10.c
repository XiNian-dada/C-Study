#include <stdio.h>
/*
 * 题目描述：分数化小数
 * 
 * 输入：
 * - 第1行: 整数n, 表示测试数据的组数
 * - 随后n行: 每行包含3个正整数a, b, c，以空格隔开
 * 
 * 输出：
 * - n行: 每行一个字符串，表示a/b的小数形式，精确到小数点后c位（不四舍五入）
 * 
 * 要求：
 * - 计算a/b的小数形式
 * - 精确到小数点后c位，不进行四舍五入
 * - 输出格式为"整数部分.小数部分"
 * 
 * 示例：
 * 输入: 
 * 3
 * 2 3 20
 * 2 6 10
 * 6 7 3
 * 输出:
 * 0.66666666666666666666
 * 0.3333333333
 * 0.857
 * 
 * 解释:
 * 2/3 = 0.666666... 保留20位小数
 * 2/6 = 0.333333... 保留10位小数
 * 6/7 ≈ 0.857142... 保留3位小数（不四舍五入）
 * 
 * 约束：
 * - 2 <= a, b, c <= 1000
 */
double divide(int a,int b,int count){
    if (a > b){ //先计算个位数的
        printf("%d.", a/b);
        a = a%b;
    }else{
        printf("0.");
    }
    for (int i = 0; i < count; i++)
    {
        printf("%d", a*10/b); //然后按照除法的原则进行循环相除，输出结果
        a = a*10%b;
    }
    printf ("\n");
}
int main(){
    int a,b,c;
    int n;
    scanf("%d",&n);
    for (int i = 0; i < n; i++)
    {
        scanf("%d %d %d",&a,&b,&c);
        divide(a,b,c);
    }
}