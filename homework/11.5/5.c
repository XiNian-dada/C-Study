#include <stdio.h>
/*
 * 题目描述：吃饭各有多少人（求解三元一次方程）
 *
 * 输入：
 * - 第1行: 整数n, 表示测试数据的组数
 * - 随后n行: 每行两个整数x, y，分别表示总人数和总花费，用空格隔开
 *
 * 输出：
 * - 对于每组数据，输出所有可能解：
 *   每行一个解，按男人数、女人数、小孩数顺序输出，用空格隔开
 *   按男人数从小到大排序，男人数相同按女人数排序，再相同按小孩数排序
 *
 * 要求：
 * - 设男人、女人、小孩人数分别为a, b, c
 * - 满足方程: a + b + c = x 且 3a + 2b + c = y
 * - 输出所有非负整数解，并按指定顺序排序
 *
 * 示例：
 * 输入:
 * 1
 * 5 10
 * 输出:
 * 0 5 0
 * 1 3 1
 * 2 1 2
 *
 * 解释:
 * 总人数5，总花费10元
 * 可能情况：0男5女0孩，1男3女1孩，2男1女2孩
 *
 * 约束：
 * - 输入数据保证有解或输出空
 */
int main(){ 
    int n;
    scanf ("%d", &n);
    for (int i = 1; i <= n; i++){
        int x,y;
        scanf ("%d %d", &x, &y);
        for(int j = 0; j <= y ; j++){
            for (int k = 0; k <= y; k++){
                for (int l = 0; l <= y; l++){
                    if (3*j + 2*k + l == y){
                        if(j + k + l ==x){
                            printf("%d %d %d\n", j, k, l); //使劲循环
                        }
                        
                    }else{continue;}
                }
            }
        }
    }


}