#include <stdio.h>
/*
 * 题目描述：统计7进制数中的数字
 * 
 * 输入：
 * - 一行: 两个用空格分隔的整数m和n
 *   - m: 十进制正整数 (1 ≤ m ≤ 10⁹)
 *   - n: 指定数字 (0 ≤ n ≤ 6)
 * 
 * 输出：
 * - 一个整数: 表示m的7进制表示中数字n的出现次数
 * 
 * 要求：
 * - 将十进制数m转换为7进制
 * - 统计在7进制表示中数字n出现的次数
 * 
 * 示例：
 * 输入: "100 2"
 * 输出: 2
 * 解释: 100的7进制是202，数字2出现了2次
 * 
 * 输入: "10 6"
 * 输出: 0
 * 解释: 10的7进制是13，数字6出现0次
 * 
 * 约束：
 * - 1 ≤ m ≤ 10⁹
 * - 0 ≤ n ≤ 6
 */
int main(){
    int m,n,count=0;
    int carry[100] = {0},index = 0; //转化为7进制保存到列表里
    scanf("%d%d",&m,&n);
    while (m > 0){
        carry[index++] = m % 7;
        m /= 7;
    }
    for (int i = 0; i <= index - 1; i++){
        
        if (carry[i] == n){ //判断符合条件的个数
            count++;
        }
    }
    printf("%d\n",count);
}